#!/usr/bin/env node

const { spawn, exec } = require('child_process');
const fs = require('fs');
const path = require('path');

console.log('ğŸš€ Nadar Vercel Deployment Pipeline...\n');

let expoProcess = null;

// Cleanup function
function cleanup() {
  console.log('\nğŸ§¹ Cleaning up processes...');
  if (expoProcess) {
    expoProcess.kill();
    console.log('âœ… Expo stopped');
  }
  process.exit(0);
}

// Handle cleanup on exit
process.on('SIGINT', cleanup);
process.on('SIGTERM', cleanup);

function runCommand(command, cwd = process.cwd()) {
  return new Promise((resolve, reject) => {
    // Use npx for vercel commands to avoid PATH issues
    const actualCommand = command.replace(/^vercel\b/, 'npx vercel');
    console.log(`ğŸ”§ Running: ${actualCommand}`);
    exec(actualCommand, { cwd }, (error, stdout, stderr) => {
      if (error) {
        console.error(`âŒ Error: ${error.message}`);
        reject(error);
        return;
      }
      if (stderr) {
        console.log(`âš ï¸  ${stderr}`);
      }
      console.log(stdout);
      resolve(stdout);
    });
  });
}

async function deployToVercel() {
  console.log('ğŸŒ Deploying server to Vercel...');

  try {
    // Check if vercel CLI is installed
    try {
      await runCommand('vercel --version');
    } catch {
      console.log('ğŸ“¦ Installing Vercel CLI...');
      await runCommand('npm install -g vercel');
    }

    // Check if user is logged in
    try {
      await runCommand('vercel whoami');
    } catch {
      console.log('ğŸ” Please login to Vercel first:');
      console.log('   Run: vercel login');
      throw new Error('Not logged in to Vercel');
    }

    // Deploy to Vercel
    console.log('ğŸš€ Deploying to Vercel...');
    const deployOutput = await runCommand('vercel --prod --yes');

    // Extract URL from output
    const urlMatch = deployOutput.match(/https:\/\/[^\s\n]+/);
    if (!urlMatch) {
      console.log('Deploy output:', deployOutput);
      throw new Error('Could not extract deployment URL from Vercel output');
    }

    const appUrl = urlMatch[0].replace(/\/$/, ''); // Remove trailing slash
    console.log(`âœ… Deployment successful!`);
    console.log(`ğŸŒ Server URL: ${appUrl}`);

    return appUrl;

  } catch (error) {
    console.error('âŒ Deployment failed:', error.message);
    throw error;
  }
}

function updateAppConfig(serverUrl) {
  console.log(`ğŸ“ Updating app configuration to use: ${serverUrl}`);
  
  const envPath = path.join(__dirname, 'app', '.env');
  const envContent = `# Auto-generated by deploy-vercel.js
EXPO_PUBLIC_API_BASE=${serverUrl}
`;
  
  fs.writeFileSync(envPath, envContent);
  console.log('âœ… App configuration updated');
}

function startExpo() {
  console.log('ğŸ“± Starting Expo with tunnel...');
  
  expoProcess = spawn('npx', ['expo', 'start', '--tunnel'], {
    cwd: path.join(__dirname, 'app'),
    stdio: ['ignore', 'pipe', 'pipe'],
    env: { ...process.env, FORCE_COLOR: '1' }
  });
  
  expoProcess.stdout.on('data', (data) => {
    const output = data.toString();
    console.log('EXPO:', output.trim());
  });
  
  expoProcess.stderr.on('data', (data) => {
    console.log('EXPO ERROR:', data.toString().trim());
  });
}

async function main() {
  try {
    // Step 1: Deploy to Vercel
    const serverUrl = await deployToVercel();
    
    // Step 2: Update app configuration
    updateAppConfig(serverUrl);
    
    // Step 3: Start Expo
    startExpo();
    
    console.log('\nğŸ‰ Deployment pipeline complete!');
    console.log('ğŸ“‹ Summary:');
    console.log(`   ğŸŒ Server: ${serverUrl}`);
    console.log('   ğŸ“± Expo: Starting with tunnel mode');
    console.log('\nâ³ Wait for Expo QR code to appear, then scan to test!');
    console.log('ğŸ’¡ Your server is now globally accessible!');
    console.log('ğŸ’¡ Press Ctrl+C to stop Expo (server will keep running)\n');
    
  } catch (error) {
    console.error('âŒ Pipeline failed:', error.message);
    cleanup();
  }
}

main();
